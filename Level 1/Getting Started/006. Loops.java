import java.util.*;

public class Main {

	public static void main(String[] args) {

		int num = 1;

		// while loop

		while(num <= 10) {
			System.out.println(num);
			num++;
		}
		System.out.println("Done");


		// for loop

		for(int i = 1; i <= 10; i++) {
			System.out.println(i);
		}
		System.out.println("Done");

	}
}